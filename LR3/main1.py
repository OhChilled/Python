# Приклад довільного рядка
my_string = "Це є довільний рядок для прикладу"

# Визначення індексу середини слова
middle_index = len(my_string) // 2

# Зрізи кожного другого символу, починаючи з 5-го і до середини слова
sliced_string = my_string[4:middle_index:2]

# Виведення результату
print("Зрізи кожного другого символу з 5-го до середини слова:", sliced_string)
